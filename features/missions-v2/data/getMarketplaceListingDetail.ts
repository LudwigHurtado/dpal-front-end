export function findMarketplaceListingById(id: string): MarketplaceListing | undefined {
  return MARKETPLACE_SAMPLE_LISTINGS.find((l) => l.id === id);
}

export function getMarketplaceListingDetail(id: string): MarketplaceListingDetail | null {
  const base = findMarketplaceListingById(id);
  if (!base) return null;
  const extra = EXTRA[id] ?? {};
  const merged: MarketplaceListingDetail = {
    ...base,
    ...BASE_DETAIL,
    fullDescription: base.summary,
    categoryLabel: 'General',
    missionTypeLabel: 'Custom mission',
    deadlineLabel: 'See coordinator',
    proofRequirements: ['Photos or notes as specified in V2'],
    safetyNotes: 'Follow local laws and site rules; stop if unsafe.',
    checklist: ['Read brief in workspace', 'Complete tasks', 'Submit proof'],
    maxParticipants: base.slotsOpen ?? BASE_DETAIL.maxParticipants,
    participantCount: BASE_DETAIL.participantCount,
    trustLevel: BASE_DETAIL.trustLevel,
    visibilityLabel: BASE_DETAIL.visibilityLabel,
    validationNote: BASE_DETAIL.validationNote,
    formatLabel: BASE_DETAIL.formatLabel,
    realmLabel: BASE_DETAIL.realmLabel,
    linkedReportId: BASE_DETAIL.linkedReportId,
    displayStatus: BASE_DETAIL.displayStatus,
    ...extra,
  };
  return merged;
}
